char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	n;

	i = ft_strlen(dest);
	n = 0;
	while (src[n])
		dest[i + n] = src[n++];
	dest[i + n] = '\0';
	return (dest);
}
